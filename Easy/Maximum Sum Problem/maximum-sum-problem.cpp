class Solution
{
    public:
        int maxSum(int n)
        {
            //code here.
            if(n<=1){
                return n;
            }
            int a=maxSum(n/2);
            int b=maxSum(n/3);
            int c=maxSum(n/4);
            return max(n,a+b+c);
        }
};

