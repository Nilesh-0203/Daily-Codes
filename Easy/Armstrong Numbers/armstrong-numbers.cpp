class Solution {
  public:
    string armstrongNumber(int n) {
        // code here
        int ans=0;
        int m=n;
        while(m){
            int t=m%10;
            ans+=pow(t,3);
            m=m/10;
        }
        return ans==n ? "true" : "false";
    }
};

