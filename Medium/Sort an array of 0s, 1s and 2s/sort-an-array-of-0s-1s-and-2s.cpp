class Solution
{
    public:
    void sort012(int a[], int n)
    {
        // code here 
        int i=0,j=n-1,k=0;
        while(k<=j){
            if(a[k]==0){
                swap(a[i],a[k]);
                k++;
                i++;
            }
            else if(a[k]==1){
                k++;
            }
            else{
                swap(a[j],a[k]);
                j--;
            }
        }
    }
    
};

