class Solution {
  public:
    Node* reverse(Node* head){
        Node* curr=head;
        Node* prev=NULL;
        Node* next=NULL;
        while(curr!=NULL){
            next=curr->next;
            curr->next=prev;
            prev=curr;
            curr=next;
        }
        return prev;
    }
    void insertAtEnd(Node* &head,Node* &tail,int data){
        Node* newNode=new Node(data);
        if(head==NULL){
            head=newNode;
            tail=newNode;
        }
        else{
            tail->next=newNode;
            tail=newNode;
        }
    }
    Node* addOne(Node* head) {
        // Your Code here
        // return head of list after adding one
        head=reverse(head);
        Node* ansHead=NULL;
        Node* ansTail=NULL;
        int carry=1;
        Node* temp=head;
        while(carry!=0 || temp!=NULL){
            int val=0;
            if(temp!=NULL){
               val=temp->data;
               temp=temp->next;
            }
            int sum=val+carry;
            int digit=sum%10;
            carry=sum/10;
            
            insertAtEnd(ansHead,ansTail,digit);
        }
        ansHead=reverse(ansHead);
        return ansHead;
    }
};

